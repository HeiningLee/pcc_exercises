#sort
print('\nSORT')
cars = ['bmw','audi','toyota','subaru']
cars.sort()
print(cars)
cars.sort(reverse = 1)
print(cars)
cars.sort()
print(cars)

#sorted
print('\nSORTED')
cars = ['bmw','audi','toyota','subaru']
print('Here is the original list:')
print(cars)

print('Here is the sorted list:')
print(sorted(cars))

print('Here is the original list again:')
print(cars)


#reverse
print('\nREVERSE')
cars = ['bmw','audi','toyota','subaru']
print(cars)
cars.reverse()
print(cars)
